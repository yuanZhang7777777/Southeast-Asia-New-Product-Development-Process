const workbook = Workbook.create();

function addSheet(name, rows, widths = []) {
  const sheet = workbook.worksheets.add(name);
  const rowCount = rows.length;
  const colCount = rows[0].length;
  const lastCol = String.fromCharCode("A".charCodeAt(0) + colCount - 1);
  const range = sheet.getRange(`A1:${lastCol}${rowCount}`);
  range.values = rows;
  sheet.getRange(`A1:${lastCol}1`).format = {
    fill: "#1F4E78",
    font: { bold: true, color: "#FFFFFF" },
    wrapText: true,
  };
  range.format.borders = { preset: "all", style: "thin", color: "#D9E2F3" };
  range.format.wrapText = true;
  range.format.font = { name: "Microsoft YaHei", size: 10 };
  sheet.freezePanes.freezeRows(1);
  sheet.showGridLines = false;
  widths.forEach((width, index) => {
    if (!width) return;
    const col = String.fromCharCode("A".charCodeAt(0) + index);
    sheet.getRange(`${col}1:${col}${rowCount}`).format.columnWidth = width;
  });
  sheet.getRange(`A1:${lastCol}${rowCount}`).format.autofitRows();
  return sheet;
}

